package BLService;

public class Restaurant {
	String name;   //店名
	String imgURL;   //图片地址
	String address;  //店的地址
	String rate;   //网友评分
	String comment;   //网友点评
	public String getName() {
		return name;
	}
	public String getImgURL() {
		return imgURL;
	}
	public String getAddress() {
		return address;
	}
	public String getRate() {
		return rate;
	}
	public String getComment() {
		return comment;
	}
	
	
}
